package org.nutz.el;

public enum ElSymbolType {

	/**
	 * 左圆括弧
	 */
	LEFT_PARENTHESIS,

	/**
	 * 右圆括弧
	 */
	RIGHT_PARENTHESIS,

	/**
	 * 左方括弧
	 */
	LEFT_BRACKET,

	/**
	 * 右方括弧
	 */
	RIGHT_BRACKET,

	/**
	 * 逗号
	 */
	COMMA,

	/**
	 * 字符串
	 */
	STRING,

	/**
	 * 变量
	 */
	VAR,

	/**
	 * 浮点
	 */
	FLOAT,

	/**
	 * 整型
	 */
	INT,

	/**
	 * 长整型
	 */
	LONG,

	/**
	 * 布尔
	 */
	BOOL,

	/**
	 * null
	 */
	NULL,

	/**
	 * 未定义
	 */
	UNDEFINED,

	/**
	 * 操作符
	 */
	OPT,

	/**
	 * 条件表达式判断符号
	 */
	CONDITIONAL_TEST,

	/**
	 * 条件表达式分隔符号
	 */
	CONDITIONAL_SEP

}
