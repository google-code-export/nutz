package org.nutz.ioc.meta;

public class IocField {

	private String name;

	private IocValue value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public IocValue getValue() {
		return value;
	}

	public void setValue(IocValue value) {
		this.value = value;
	}

}
